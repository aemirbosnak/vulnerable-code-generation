#include <stdio.h>
#include <math.h>

int main() {
    float peacefulness = 0;
    float num1, num2, sum;

    printf("Welcome to the Peaceful Math Exercise!\n");

    // Calculate the area of a virtual pond
    printf("The virtual pond has a diameter of 10 meters. What is its area?\n");
    scanf("%f", &num1);
    sum = 3.14159 * num1 * num1;
    peacefulness += sum;

    // Calculate the volume of a virtual mountain
    printf("The virtual mountain has a height of 500 meters. What is its volume?\n");
    scanf("%f", &num2);
    sum = num2 * num2 * num2;
    peacefulness += sum;

    // Calculate the distance between two virtual points
    printf("The distance between two virtual points is 100 meters. What is the distance?\n");
    scanf("%f", &num1);
    sum = num1 * 2;
    peacefulness += sum;

    // Calculate the midpoint of a virtual line
    printf("The virtual line has a length of 1000 meters. What is its midpoint?\n");
    scanf("%f", &num1);
    sum = num1 + 500;
    peacefulness += sum;

    // Calculate the slope of a virtual line
    printf("The virtual line has a slope of 2. What is its slope?\n");
    scanf("%f", &num1);
    sum = num1 * 2;
    peacefulness += sum;

    // Calculate the equation of a virtual line
    printf("The virtual line has the equation y = 2x + 1. What is its equation?\n");
    scanf("%f", &num1);
    sum = num1 * 2 + 1;
    peacefulness += sum;

    // Print the final peacefulness score
    printf("Your peacefulness score is: %f\n", peacefulness);

    return 0;
}