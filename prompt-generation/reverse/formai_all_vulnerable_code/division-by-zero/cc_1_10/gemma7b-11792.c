#include <stdio.h>
#include <stdlib.h>

int main() {
  int a = 5;
  int b = 0;
  int c = 10;

  if (a == 5) {
    printf("a is 5\n");
  } else {
    printf("a is not 5\n");
  }

  if (b == 0) {
    printf("b is 0\n");
  } else {
    printf("b is not 0\n");
  }

  if (c == 10) {
    printf("c is 10\n");
  } else {
    printf("c is not 10\n");
  }

  if (a + b == 10) {
    printf("a + b is 10\n");
  } else {
    printf("a + b is not 10\n");
  }

  if (c - a == 5) {
    printf("c - a is 5\n");
  } else {
    printf("c - a is not 5\n");
  }

  if (a * b == 0) {
    printf("a * b is 0\n");
  } else {
    printf("a * b is not 0\n");
  }

  if (a / b == 2) {
    printf("a / b is 2\n");
  } else {
    printf("a / b is not 2\n");
  }

  return 0;
}