#include <stdio.h>

// Declare the variables we will be using
int a, b, c, d, e, f, g, h, i, j, k, l, m, n, o, p, q, r, s, t, u, v, w, x, y, z;

// Define the main function
int main(void) {
    // Prompt the user to input a value for a
    printf("Enter a value for a: ");
    scanf("%d", &a);

    // Prompt the user to input a value for b
    printf("Enter a value for b: ");
    scanf("%d", &b);

    // Calculate the value of c
    c = a + b;

    // Calculate the value of d
    d = a - b;

    // Calculate the value of e
    e = a * b;

    // Calculate the value of f
    f = a / b;

    // Calculate the value of g
    g = a % b;

    // Calculate the value of h
    h = a & b;

    // Calculate the value of i
    i = a | b;

    // Calculate the value of j
    j = a ^ b;

    // Calculate the value of k
    k = ~a;

    // Calculate the value of l
    l = a << b;

    // Calculate the value of m
    m = a >> b;

    // Print the values of c, d, e, f, g, h, i, j, k, l, and m
    printf("c = %d\n", c);
    printf("d = %d\n", d);
    printf("e = %d\n", e);
    printf("f = %d\n", f);
    printf("g = %d\n", g);
    printf("h = %d\n", h);
    printf("i = %d\n", i);
    printf("j = %d\n", j);
    printf("k = %d\n", k);
    printf("l = %d\n", l);
    printf("m = %d\n", m);

    return 0;
}