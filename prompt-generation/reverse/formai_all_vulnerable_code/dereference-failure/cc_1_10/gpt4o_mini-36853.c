int main() {
    char data[] = {'a', 'b', 'c', 'd', 'e', 'f'};
    int frequency[] = {5, 9, 12, 13, 16, 45};
    int size = sizeof(data) / sizeof(data[0]);

    struct Node* root = buildHuffmanTree(data, frequency, size);
    int arr[100], top = 0;

    printf("Huffman Codes:\n");
    printHuffmanCodes(root, arr, top);

    return 0;
}