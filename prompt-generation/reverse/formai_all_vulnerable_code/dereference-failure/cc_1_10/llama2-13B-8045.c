#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <time.h>
#include <math.h>

#define MAGIC_NUMBER 42

struct Node {
    char *word;
    int frequency;
    struct Node *next;
};

struct Trie {
    struct Node *root;
};

void initTrie(struct Trie *trie) {
    trie->root = NULL;
}

void addWord(struct Trie *trie, char *word) {
    struct Node *node = (struct Node *)calloc(1, sizeof(struct Node));
    node->word = word;
    node->frequency = 1;
    node->next = trie->root;
    trie->root = node;
}

void printFrequencies(struct Trie *trie) {
    struct Node *current = trie->root;
    while (current != NULL) {
        printf("%s: %d\n", current->word, current->frequency);
        current = current->next;
    }
}

void generatePoem(struct Trie *trie, int length) {
    struct Node *current = trie->root;
    char buffer[100];
    int i = 0;
    while (i < length && current != NULL) {
        buffer[i++] = current->word[rand() % strlen(current->word)];
        current = current->next;
    }
    printf("A poem of %d words, generated by the Trie of Genius:\n%s\n", length, buffer);
}

int main() {
    struct Trie *trie = (struct Trie *)calloc(1, sizeof(struct Trie));
    addWord(trie, "genius");
    addWord(trie, "code");
    addWord(trie, "snippet");
    addWord(trie, "poem");
    addWord(trie, "trie");
    printFrequencies(trie);
    generatePoem(trie, 10);
    return 0;
}