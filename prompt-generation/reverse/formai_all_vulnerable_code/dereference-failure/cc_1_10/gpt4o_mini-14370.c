int main(int argc, char *argv[]) {
    char input[MAX_INPUT_SIZE];
    int scale = 1;

    if (argc > 2) {
        fprintf(stderr, "Usage: %s [scale (default: 1)]\n", argv[0]);
        return 1;
    }
    
    if (argc == 2) {
        scale = atoi(argv[1]);
        if (scale < 1 || scale > 10) {
            fprintf(stderr, "Scale must be between 1 and 10\n");
            return 1;
        }
    }
    
    printf("Enter a string to convert to ASCII art (max %d characters):\n", MAX_INPUT_SIZE - 1);
    
    if (fgets(input, sizeof(input), stdin) == NULL) {
        fprintf(stderr, "Error reading input.\n");
        return 1;
    }

    // Remove trailing newline if it exists
    input[strcspn(input, "\n")] = 0;

    // Validate input length
    if (strlen(input) == 0 || strlen(input) > MAX_INPUT_SIZE - 1) {
        fprintf(stderr, "Input must be between 1 and %d characters.\n", MAX_INPUT_SIZE - 1);
        return 1;
    }

    printf("\nASCII Art:\n");
    printAsciiArt(input, scale);
    return 0;
}

