// Function to convert a single character to Morse code
char *char_to_morse(char c) {
    if (c >= 'A' && c <= 'Z') {
        c += 32; // Convert to lowercase
    }

    int index = c - 'a';
    if (index >= 0 && index < 26) {
        return morse_code[index];
    } else {
        return NULL;
    }
}

// Function to convert a string to Morse code
char *string_to_morse(char *str) {
    int len = strlen(str);
    char *morse_code = malloc(len * 5 + 1); // Allocate enough space for the Morse code representation
    int i, index;

    for (i = 0; i < len; i++) {
        char *morse = char_to_morse(str[i]);
        if (morse) {
            strcat(morse_code, morse);
            strcat(morse_code, " "); // Add a space between characters
        }
    }

    return morse_code;
}

// Function to print the Morse code representation of a string
void print_morse_code(char *morse_code) {
    printf("Morse code: %s\n", morse_code);
}

int main() {
    char str[] = "Hello, world!";
    char *morse_code = string_to_morse(str);
    print_morse_code(morse_code);

    free(morse_code);
    return 0;
}